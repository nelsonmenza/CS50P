"""
This program prompts the user to enter a string, stores it in the variable 'indoor,' and then
prints the lowercase version of the entered string.

Usage:
1. Run the program.
2. Enter a string when prompted.
3. The program will print the lowercase version of the entered string.
"""
# Prompt the user to enter a string and store it in the variable 'indoor'
indoor = input("Enter a string: ")
# Convert the string to lowercase and print it
print(indoor.lower())
